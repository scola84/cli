export function column(s) {
  s.build(
    s.query(
      s.select(
        s.as(
          'COLUMNS.COLUMN_NAME',
          'name'
        ),
        s.as(
          'COLUMNS.DATA_TYPE',
          'type'
        ),
        s.as(
          s.eq(
            'COLUMNS.IS_NULLABLE',
            s.value('NO')
          ),
          'required'
        ),
        s.as(
          'COLUMNS.CHARACTER_MAXIMUM_LENGTH',
          'maxlength'
        ),
        s.as(
          'COLUMNS.COLUMN_COMMENT',
          'comment'
        ),
        s.as(
          s.eq(
            'COLUMNS.COLUMN_KEY',
            s.value('PRI')
          ),
          'pk'
        ),
        s.as(
          s.replace(
            s.substring(
              'COLUMNS.COLUMN_TYPE',
              s.plus(
                s.locate(
                  s.value('('),
                  'COLUMNS.COLUMN_TYPE'
                ),
                1
              )
            ),
            s.value(')'),
            s.value('')
          ).parens(),
          s.id('values')
        )
      ),
      s.from('information_schema.COLUMNS'),
      s.where(
        s.eq(
          'COLUMNS.TABLE_NAME',
          s.value((box, data) => data.name)
        )
      )
    )
  );
}
