export function mysql(s, schema, name) {
  s.build(
    s.query(
      s.select(
        s.as(
          'COLUMNS.TABLE_NAME',
          s.id('table')
        ),
        s.as(
          'COLUMNS.COLUMN_NAME',
          'name'
        ),
        s.as(
          'COLUMNS.DATA_TYPE',
          'type'
        ),
        s.as(
          s.eq(
            'COLUMNS.IS_NULLABLE',
            s.value('NO')
          ),
          'required'
        ),
        s.as(
          'COLUMNS.CHARACTER_MAXIMUM_LENGTH',
          'maxlength'
        ),
        s.as(
          'COLUMNS.COLUMN_COMMENT',
          'options'
        ),
        s.as(
          s.eq(
            'COLUMNS.COLUMN_KEY',
            s.value('PRI')
          ),
          'primary'
        ),
        s.as(
          s.replace(
            s.substring(
              'COLUMNS.COLUMN_TYPE',
              s.plus(
                s.locate(
                  s.value('('),
                  'COLUMNS.COLUMN_TYPE'
                ),
                1
              )
            ),
            s.value(')'),
            s.value('')
          ).parens(),
          s.id('values')
        )
      ),
      s.from('information_schema.COLUMNS'),
      s.where(
        s.and(
          s.eq(
            'COLUMNS.TABLE_SCHEMA',
            s.value(schema)
          ),
          s.like(
            'COLUMNS.TABLE_NAME',
            s.value(name)
          )
        )
      ),
      s.orderBy(
        s.asc(
          s.id('columns.TABLE_NAME')
        ),
        s.asc(
          s.id('column.ORDINAL_POSITION')
        )
      )
    )
  );
}
